$(document).ready(function () {
    $('#bg-dark').click(function () {
      $('body').css('background-color', '#222');
      $('body').css('color', 'white');
    });
  
    $('#bg-light').click(function () {
      $('body').css('background-color', 'white');
      $('body').css('color', 'black');
    });
  });
  